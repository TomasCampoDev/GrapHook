using UnityEngine;
using TMPro;

/// <summary>
/// ScoreManager – sistema de puntuación modular para móvil.
/// El Canvas de UI persiste entre escenas gracias a DontDestroyOnLoad.
/// Coloca este script en un GameObject vacío llamado "ScoreManager".
/// </summary>
public class ScoreManager : MonoBehaviour
{
    // ?? Singleton ??????????????????????????????????????????????????????????????
    public static ScoreManager Instance { get; private set; }

    // ?? Referencias ???????????????????????????????????????????????????????????
    [Header("Referencias")]
    [Tooltip("(Opcional) Arrastra aquí un TextMeshProUGUI ya existente. Si se deja vacío se crea automáticamente.")]
    [SerializeField] private TextMeshProUGUI scoreText;

    [Tooltip("(Opcional) Font Asset generado con Font Asset Creator (archivo .asset). " +
             "Si se deja vacío usa la fuente por defecto de TMP. El color sigue siendo el de abajo.")]
    [SerializeField] private TMP_FontAsset fontAsset;

    // ?? Apariencia ????????????????????????????????????????????????????????????
    [Header("Apariencia")]
    [Tooltip("Tamańo de la fuente en puntos")]
    [SerializeField] private float fontSize = 72f;

    [Tooltip("Color del texto (se aplica siempre, independientemente de la fuente)")]
    [SerializeField] private Color textColor = new Color(0.2f, 1f, 0.5f); // verde digital

    [Tooltip("Prefijo que aparece antes del número (puede dejarse vacío)")]
    [SerializeField] private string prefix = "";

    // ?? Posición y tamańo del RectTransform ???????????????????????????????????
    [Header("Posición (Canvas en modo Screen Space – Overlay)")]
    [Tooltip("Anchor preset. Para esquina superior izquierda usa (0,1) en ambos")]
    [SerializeField] private Vector2 anchorMin = new Vector2(0f, 1f);
    [SerializeField] private Vector2 anchorMax = new Vector2(0f, 1f);
    [SerializeField] private Vector2 pivot = new Vector2(0f, 1f);

    [Tooltip("Desplazamiento en píxeles desde la esquina anclada")]
    [SerializeField] private Vector2 offsetFromCorner = new Vector2(20f, -20f);

    [Tooltip("Tamańo del área de texto en píxeles")]
    [SerializeField] private Vector2 rectSize = new Vector2(400f, 100f);

    // ?? Animación de puntos ???????????????????????????????????????????????????
    [Header("Animación (opcional)")]
    [Tooltip("Escala el texto brevemente al sumar puntos")]
    [SerializeField] private bool animateOnScore = true;

    [Tooltip("Escala máxima durante el pop")]
    [SerializeField] private float popScale = 1.25f;

    [Tooltip("Velocidad del pop")]
    [SerializeField] private float popSpeed = 8f;

    // ?? Puntuación ?????????????????????????????????????????????????????????????
    [Header("Puntuación")]
    [Tooltip("Puntuación actual. Puedes modificarla directamente desde el Inspector o desde otros scripts.")]
    public int currentScore = 0;

    // ?? Estado interno ?????????????????????????????????????????????????????????
    private float targetScale = 1f;
    private float currentScaleValue = 1f;

    // Canvas propio de la UI persistente
    private Canvas uiCanvas;

    // ???????????????????????????????????????????????????????????????????????????
    // Unity Lifecycle
    // ???????????????????????????????????????????????????????????????????????????

    private void Awake()
    {
        // Si ya existe otra instancia, esta se destruye: la UI no se duplica
        if (Instance != null && Instance != this)
        {
            Destroy(gameObject);
            return;
        }

        Instance = this;

        // El ScoreManager sobrevive a los cambios de escena...
        DontDestroyOnLoad(gameObject);
    }

    private void Start()
    {
        if (scoreText == null)
            CreatePersistentCanvas();   // crea su propio Canvas persistente
        else
            MakeExistingCanvasPersistent(); // marca el Canvas del texto ya asignado

        ApplyVisualSettings();
        UpdateDisplay();
    }

    private void Update()
    {
        if (!animateOnScore) return;

        currentScaleValue = Mathf.Lerp(currentScaleValue, targetScale, Time.deltaTime * popSpeed);
        if (scoreText != null)
            scoreText.transform.localScale = Vector3.one * currentScaleValue;

        if (Mathf.Abs(currentScaleValue - 1f) < 0.01f)
            targetScale = 1f;
    }

    // ???????????????????????????????????????????????????????????????????????????
    // Gestión del Canvas persistente
    // ???????????????????????????????????????????????????????????????????????????

    /// <summary>
    /// Crea un Canvas nuevo dedicado exclusivamente a la UI del score,
    /// separado del Canvas de la escena para que no se destruya con ella.
    /// </summary>
    private void CreatePersistentCanvas()
    {
        // Canvas dedicado para la UI persistente
        GameObject canvasGO = new GameObject("ScoreCanvas_Persistent");
        uiCanvas = canvasGO.AddComponent<Canvas>();
        uiCanvas.renderMode = RenderMode.ScreenSpaceOverlay;
        // SortingOrder alto para que siempre quede encima de otros canvas
        uiCanvas.sortingOrder = 99;

        var scaler = canvasGO.AddComponent<UnityEngine.UI.CanvasScaler>();
        scaler.uiScaleMode = UnityEngine.UI.CanvasScaler.ScaleMode.ScaleWithScreenSize;
        scaler.referenceResolution = new Vector2(1080f, 1920f);
        scaler.matchWidthOrHeight = 0.5f;

        canvasGO.AddComponent<UnityEngine.UI.GraphicRaycaster>();

        // Este Canvas tampoco se destruye al cambiar de escena
        DontDestroyOnLoad(canvasGO);

        // Crea el texto dentro del canvas persistente
        GameObject textGO = new GameObject("ScoreText");
        textGO.transform.SetParent(canvasGO.transform, false);
        scoreText = textGO.AddComponent<TextMeshProUGUI>();
        scoreText.enableWordWrapping = false;
        scoreText.alignment = TextAlignmentOptions.Left;
    }

    /// <summary>
    /// Cuando scoreText ya está asignado desde el Inspector, sube por la jerarquía
    /// hasta encontrar el Canvas raíz y lo marca como persistente.
    /// </summary>
    private void MakeExistingCanvasPersistent()
    {
        // Busca el Canvas raíz del texto asignado
        Canvas canvas = scoreText.GetComponentInParent<Canvas>();
        if (canvas == null) return;

        // Si el Canvas tiene padre, necesitamos el raíz (DontDestroyOnLoad solo funciona en raíz)
        Transform root = canvas.transform;
        while (root.parent != null)
            root = root.parent;

        DontDestroyOnLoad(root.gameObject);
        uiCanvas = root.GetComponent<Canvas>();
    }

    // ???????????????????????????????????????????????????????????????????????????
    // API pública
    // ???????????????????????????????????????????????????????????????????????????

    /// <summary>Suma puntos (por defecto 1). Llámalo desde tu método de plataformas.</summary>
    public void AddScore(int amount = 1)
    {
        currentScore += amount;
        UpdateDisplay();
        TriggerPop();
    }

    /// <summary>Resta puntos. No baja de cero.</summary>
    public void SubtractScore(int amount = 1)
    {
        currentScore = Mathf.Max(0, currentScore - amount);
        UpdateDisplay();
    }

    /// <summary>Fija la puntuación a un valor concreto.</summary>
    public void SetScore(int value)
    {
        currentScore = Mathf.Max(0, value);
        UpdateDisplay();
    }

    /// <summary>Reinicia la puntuación a cero.</summary>
    public void ResetScore()
    {
        currentScore = 0;
        UpdateDisplay();
    }

    /// <summary>Devuelve la puntuación actual.</summary>
    public int GetScore() => currentScore;

    // ???????????????????????????????????????????????????????????????????????????
    // Métodos privados
    // ???????????????????????????????????????????????????????????????????????????

    private void UpdateDisplay()
    {
        if (scoreText == null) return;
        scoreText.text = prefix + currentScore.ToString();
    }

    private void TriggerPop()
    {
        if (!animateOnScore) return;
        currentScaleValue = popScale;
        targetScale = 1f;
    }

    private void ApplyVisualSettings()
    {
        if (scoreText == null) return;

        scoreText.fontSize = fontSize;
        scoreText.color = textColor;

        if (fontAsset != null)
            scoreText.font = fontAsset;

        RectTransform rt = scoreText.GetComponent<RectTransform>();
        if (rt == null) return;

        rt.anchorMin = anchorMin;
        rt.anchorMax = anchorMax;
        rt.pivot = pivot;
        rt.sizeDelta = rectSize;
        rt.anchoredPosition = offsetFromCorner;
    }

    // ???????????????????????????????????????????????????????????????????????????
    // Editor helper
    // ???????????????????????????????????????????????????????????????????????????
#if UNITY_EDITOR
    private void OnValidate()
    {
        if (!Application.isPlaying) return;
        ApplyVisualSettings();
        UpdateDisplay();
    }
#endif
}